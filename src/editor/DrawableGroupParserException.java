package editor;

public class DrawableGroupParserException extends RuntimeException {
	private static final long serialVersionUID = -6420546245130220088L;

	public DrawableGroupParserException() {
		super();
	}

	public DrawableGroupParserException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public DrawableGroupParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public DrawableGroupParserException(String message) {
		super(message);
	}

	public DrawableGroupParserException(Throwable cause) {
		super(cause);
	}


}
